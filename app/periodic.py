def getElem(symbol):
	symbol = symbol.title()

	try:
		return table.index(symbol)
	except:
		return None

def getElemList(word):
	return getElemRec(word, [])

def getElemRec(word, elemNums):
	if len(word) == 0:
		return elemNums

	ret = None

	n = getElem(word[0] + word[1]) if len(word) > 1 else None
	if n:
		ret = getElemRec(word[2:], elemNums + [n])

	if ret == None:
		n = getElem(word[0])
		if n:
			ret = getElemRec(word[1:], elemNums + [n])

	return ret

def getElemNameList(elemList):
	elemNameList = []
	if elemList == None:
		return None
	for num in elemList:
		elemNameList.append({'sym': table[num], 'num': num, 
			'mass': masses[num], 'name': names[num]})
	return elemNameList
	